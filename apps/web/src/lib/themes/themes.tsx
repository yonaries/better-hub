import type { ThemeDefinition, ShikiThemeInput, ShikiTheme } from "./types";

function convertToShikiTheme(input: ShikiThemeInput, mode: "light" | "dark" = "dark"): ShikiTheme {
	const {
		foreground,
		constant,
		string,
		comment,
		keyword,
		parameter,
		function: fn,
		stringExpression,
		punctuation,
		link,
		number,
		property,
		objectLiteral,
		highlight,
		highlightBorder,
		diffDeleted,
		diffInserted,
	} = input;

	return {
		name: "custom-theme",
		type: mode,
		colors: {
			"editor.foreground": foreground,
			"editor.background": "#000000",
			...(highlight && { "editor.lineHighlightBackground": highlight }),
			...(highlightBorder && { "editor.lineHighlightBorder": highlightBorder }),
			...(diffDeleted && { "diffEditor.removedTextBackground": diffDeleted }),
			...(diffInserted && { "diffEditor.insertedTextBackground": diffInserted }),
		},
		tokenColors: [
			{
				scope: ["comment", "punctuation.definition.comment"],
				settings: { foreground: comment ?? foreground },
			},
			{
				scope: ["string", "string.quoted"],
				settings: { foreground: string ?? foreground },
			},
			{
				scope: ["string.template", "string.interpolated"],
				settings: { foreground: stringExpression ?? string ?? foreground },
			},
			{
				scope: ["constant", "constant.numeric", "constant.language"],
				settings: { foreground: constant ?? foreground },
			},
			{
				scope: ["keyword", "storage", "storage.type"],
				settings: { foreground: keyword ?? foreground },
			},
			{
				scope: ["variable.parameter", "meta.function.parameters"],
				settings: { foreground: parameter ?? foreground },
			},
			{
				scope: ["entity.name.function", "support.function"],
				settings: { foreground: fn ?? foreground },
			},
			{
				scope: ["punctuation", "meta.brace"],
				settings: { foreground: punctuation ?? foreground },
			},
			{
				scope: ["markup.underline.link", "string.other.link"],
				settings: { foreground: link ?? foreground },
			},
			{
				scope: ["constant.numeric"],
				settings: { foreground: number ?? constant ?? foreground },
			},
			{
				scope: ["variable.other.property", "support.type.property-name"],
				settings: { foreground: property ?? foreground },
			},
			{
				scope: ["meta.objectliteral", "meta.object-literal.key"],
				settings: { foreground: objectLiteral ?? property ?? foreground },
			},
		],
	};
}

